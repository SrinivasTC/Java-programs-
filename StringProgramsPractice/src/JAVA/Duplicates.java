package JAVA;

import java.util.HashSet;

public class Duplicates {

	public static void main(String[] args) {
		
		

	
		
String word="srinivas";
		
		
		char sd='s';
		
		int count=0;
		
		for(int ss=0;ss<word.length();ss++) {
			
			if(word.charAt(ss)==sd) {
				
				count++;

				
			}
			
			
}
		
		System.out.println("the count of 's' character repeated in word is:"+count);
}}